<template>
  <view class="room-create">
    <view class="display-header">
      <view class="shim" />
      <view class="avatar-list">
        <Avatar
          v-for="item in avatars"
          :key="item.id"
          :item="item"
        />
        <view
          v-if="avatars.length"
          class="avatar play-btn"
          @tap="handlePlay"
        >
          æ’­
        </view>
      </view>
      <view class="shim" />
    </view>
    <view
      class="track-wrap"
      scroll-x="true"
    >
      <Track
        v-for="track in content"
        :key="track.id"
        :item="track"
      />
    </view>
    <Control />
    <SearchDrawer />
  </view>
</template>

<script>
import Taro from '@tarojs/taro'
import { computed } from 'vue'
import { useStore } from 'vuex'
import SearchDrawer from './components/searchDrawer'
import Avatar from './components/avatar'
import Track from './components/track'
import Control from './components/control'

export default {
  components: {
    Track,
    Avatar,
    Control,
    SearchDrawer
  },
  setup() {
    const store = useStore()

    const content = computed(() => {
      return store.state.live.content
    })

    const avatars = computed(() => {
      return store.state.live.editor.readers
    })

    const handlePlay = () => {
      Taro.navigateTo({
        url: '/pages/room/live/index'
      })
    }

    store.dispatch('live/initEditor')

    return {
      content,
      avatars,
      handlePlay
    }
  }
}
</script>

