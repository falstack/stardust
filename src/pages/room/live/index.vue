<template>
  <view class="room-live">
    <MsgRoom ref="roomRef" />
    <button @tap="addMessage">add+</button>
  </view>
</template>

<script>
import { ref } from 'vue'
import MsgRoom from '~/components/message/room'

export default {
  name: 'RoomLive',
  components: {
    MsgRoom
  },
  setup() {
    const roomRef = ref(null)
    const addMessage = () => {
      roomRef.value.addMessage({
        id: 1,
        type: 'bubble-msg',
        side: 'left',
        loading: true,
        content: [
          {
            type: 'txt',
            text: '123'
          }
        ],
        user: {
          id: 1,
          avatar: 'avatar',
          nickname: '冰淤'
        }
      })
    }

    return {
      roomRef,
      addMessage
    }
  }
}
</script>

